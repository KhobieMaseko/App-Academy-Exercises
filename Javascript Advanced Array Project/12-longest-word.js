/*

Write a function `longestWord(sentence)` that takes in a sentence string as an
argument. The function should return the longest word in the sentence.

You must use  `Array.forEach` in your solution.

Solve this using Array's `forEach()`, `map()`, `filter()` **OR** `reduce()`
methods.

Examples:

console.log(longestWord('where did everyone go')); // 'everyone'
console.log(longestWord('prefer simplicity over complexity')); // 'simplicity'
console.log(longestWord('')); // ''

*/

let longestWord = function(sentence) {
    // Your code here
    // Split the sentence into an array of words
    const words = sentence.split(' ');

    let longestWord = '';

    // Use forEach to iterate through the words in the array
    words.forEach(function(word) {
    // Check if the current word is longer than the current longest word
        if (word.length > longestWord.length) {
            longestWord = word;
        }
    });

    return longestWord;
};

